#include <stdio.h>

int main() {
    int i = 0;
    printf("%d\n",i); // print 0
    printf("%d\n",--i); // print -1, (minus then print i)
    printf("%d\n",i);
    printf("%d\n",i--); // print -1, then minus 1
    printf("%d\n",i); // print -2
    return 0;
}
